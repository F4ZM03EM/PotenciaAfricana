function mostrarMensagem(elementId, mensagem, tipo) {
    const el = document.getElementById(elementId);
    if (!el) return;
    el.innerHTML = mensagem;
    el.style.display = 'block';
    el.style.background = tipo === 'error' ? 'rgba(255, 46, 99, 0.3)' : 'rgba(0, 200, 100, 0.2)';
    el.style.color = tipo === 'error' ? '#ff8888' : '#88ff88';
    setTimeout(() => {
        if (el) el.style.display = 'none';
    }, 4000);
}

function atualizarContadorPartilhas() {
    const msgDiv = document.getElementById('shareCountMsg');
    if (msgDiv) {
        msgDiv.innerHTML = `Partilhas realizadas: ${compartilhamentos}/10`;
        if (compartilhamentos > 0 && compartilhamentos < 10) {
            const faltam = 10 - compartilhamentos;
            mostrarMensagem('shareStatusMsg', `✅ Falta pouco! Mais ${faltam} partilha(s) e ganhas 50% de desconto!`, 'success');
        }
    }
}

async function confirmarPartilha() {
    if (aguardandoConfirmacao) {
        mostrarMensagem('shareStatusMsg', '⚠️ Aguarda, já estás a partilhar...', 'error');
        return;
    }
    
    const shareData = {
        title: 'Conteúdo exclusivo VIP',
        text: '🔥 Entra e desfruta dos melhores conteúdos VIPs! 🔥',
        url: LINK_PARTILHA
    };
    
    try {
        aguardandoConfirmacao = true;
        tempoInicioPartilha = Date.now();
        
        const msgDiv = document.getElementById('shareStatusMsg');
        if (msgDiv) msgDiv.style.display = 'none';
        
        if (navigator.share) {
            await navigator.share(shareData);
        } else {
            const msgWhats = `🔥 Entra e desfruta dos melhores conteúdos VIPs! 🔥\n\n${LINK_PARTILHA}`;
            navigator.clipboard.writeText(msgWhats);
            mostrarMensagem('shareStatusMsg', '📋 Link copiado! Partilha no WhatsApp e volta', 'success');
        }
        
        timerTimeout = setTimeout(() => {
            const tempoDecorrido = (Date.now() - tempoInicioPartilha) / 1000;
            
            if (tempoDecorrido >= 10) {
                compartilhamentos++;
                localStorage.setItem("shares", compartilhamentos);
                atualizarContadorPartilhas();
                
                if (compartilhamentos >= 10) {
                    fecharModal();
                    document.getElementById('descontoModal').style.display = 'flex';
                } else {
                    const faltam = 10 - compartilhamentos;
                    mostrarMensagem('shareStatusMsg', `✅ Partilha confirmada! Faltam ${faltam} partilha(s) para ganhares 50% de desconto!`, 'success');
                }
            } else {
                mostrarMensagem('shareStatusMsg', `❌ A partilha não foi contada. Tens de ficar pelo menos 10 segundos fora. Tenta novamente!`, 'error');
            }
            
            aguardandoConfirmacao = false;
        }, 10000);
        
    } catch (err) {
        clearTimeout(timerTimeout);
        aguardandoConfirmacao = false;
        mostrarMensagem('shareStatusMsg', '❌ Cancelaste a partilha. Tenta novamente!', 'error');
    }
}

