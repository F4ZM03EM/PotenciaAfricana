function switchTab(tab) {
    const tabs = document.querySelectorAll('#vipArea .tab');
    const contents = document.querySelectorAll('#vipArea .tab-content');
    if (!tabs.length) return;
    
    tabs.forEach(t => t.classList.remove('active'));
    contents.forEach(c => c.classList.remove('active'));
    
    if (tab === 'vip') {
        tabs[0].classList.add('active');
        document.getElementById('tab-vip').classList.add('active');
    } else {
        tabs[1].classList.add('active');
        document.getElementById('tab-elite').classList.add('active');
    }
}

